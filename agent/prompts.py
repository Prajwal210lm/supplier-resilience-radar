def build_first_message(profile) -> str:
    return (
        "Assess the risk posed by this supplier. These figures are established facts; "
        "use them to guide your research, do not restate them as numbers in your findings.\n\n"
        f"Supplier: {profile.name} ({profile.supplier_id})\n"
        f"Country: {profile.country}, shipping from {profile.port_of_origin}\n"
        f"Category: {profile.category}\n"
        f"Spend share: {profile.spend_share}\n"
        f"Single-sourced SKUs: {profile.skus_single_sourced}\n"
        f"Line-down gap if they fail today (weeks): {profile.line_down_gap_weeks}\n"
        f"Concentration score: {profile.concentration_score}\n\n"
        "Begin your investigation."
    )
